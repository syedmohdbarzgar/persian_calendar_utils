/// (95) Is the given Nepali year "leap" in the loose sense of being longer
/// than the most common 365-day year? See the file-level note above for why
/// this is not a strict astronomical definition.
///
/// Example: `nepaliIsLeapYear(2081)` → `true` if that year totals 366 days.
bool nepaliIsLeapYear(int year) => nepaliDaysInYear(year) != 365;

/// (96) The Nepali (Devanagari) name of the weekday of a Nepali date.
String nepaliWeekdayName(NepaliDate date) => _weekdayNamesNe[date.toJdn() % 7];

/// (97) The English name of the weekday of a Nepali date.
String nepaliWeekdayNameEn(NepaliDate date) =>
    _weekdayNamesEn[date.toJdn() % 7];

/// (98) The Nepali (Devanagari) name of a Nepali month (1 to 12).
String nepaliMonthName(int month) => _nepaliMonthNamesNe[month - 1];

/// (99) The English (transliterated) name of a Nepali month.
String nepaliMonthNameEn(int month) => _nepaliMonthNamesEn[month - 1];

/// (100) Adds (or subtracts) a number of days to a Nepali date.
NepaliDate nepaliAddDays(NepaliDate date, int days) =>
    NepaliDate.fromJdn(date.toJdn() + days);

/// (101) Adds (or subtracts) a number of months; the day is clamped to the
/// last day of the target month if needed.
NepaliDate nepaliAddMonths(NepaliDate date, int months) {
  final totalMonths = date.year * 12 + (date.month - 1) + months;
  final newYear = (totalMonths - totalMonths % 12) ~/ 12;
  final newMonth = totalMonths % 12 + 1;
  final maxDay = getMonthDays(newYear, newMonth, NepaliDate.new);
  final newDay = date.dayOfMonth > maxDay ? maxDay : date.dayOfMonth;
  return NepaliDate(newYear, newMonth, newDay);
}

/// (102) Adds (or subtracts) a number of years; the day is clamped if
/// needed.
NepaliDate nepaliAddYears(NepaliDate date, int years) {
  final newYear = date.year + years;
  final maxDay = getMonthDays(newYear, date.month, NepaliDate.new);
  final newDay = date.dayOfMonth > maxDay ? maxDay : date.dayOfMonth;
  return NepaliDate(newYear, date.month, newDay);
}

/// (103) The total number of days in a Nepali year.
int nepaliDaysInYear(int year) {
  final thisYearStart = NepaliDate(year, 1, 1).toJdn();
  final nextYearStart = NepaliDate(year + 1, 1, 1).toJdn();
  return nextYearStart - thisYearStart;
}

/// (104) The day-of-year number for a Nepali date.
int nepaliDayOfYear(NepaliDate date) =>
    date.toJdn() - NepaliDate(date.year, 1, 1).toJdn() + 1;

/// (105) Is this date equal to today (based on the system clock)?
bool nepaliIsToday(NepaliDate date) => isSameDay(date, _todayNepali());

/// (106) A 6x7 grid of the days of a Nepali month, for rendering a calendar
/// widget (the week starts on Sunday, the Nepali convention). Cells outside
/// the range of the month are filled with `null`.
List<List<int?>> nepaliGetMonthGrid(int year, int month) {
  final firstDayCanonical = getFirstDayOfMonth(year, month, NepaliDate.new);
  // Map from canonical order (Monday=0) to Nepali order (Sunday=0)
  final firstDaySundayIndexed = (firstDayCanonical + 1) % 7;
  final daysInMonth = getMonthDays(year, month, NepaliDate.new);

  final grid = List.generate(6, (_) => List<int?>.filled(7, null));
  var day = 1;
  for (var week = 0; week < 6 && day <= daysInMonth; week++) {
    for (var col = 0; col < 7; col++) {
      if (week == 0 && col < firstDaySundayIndexed) continue;
      if (day > daysInMonth) continue;
      grid[week][col] = day;
      day++;
    }
  }
  return grid;
}
