// Category 5: Nepali-calendar (NepaliDate, Bikram Sambat) specific
// functions — 18 functions (#89 through #106).
//
// Range limitation: NepaliDate in persian_calendar only covers dates whose
// Gregorian equivalent falls between 1975 and 2199 (per the package's own
// documentation). Functions in this file that construct a NepaliDate for a
// year outside that range will throw, exactly as the underlying library
// does; this file does not add any extra bounds checking beyond what
// persian_calendar itself provides.
//
// Weekday convention: the Nepali week starts on Sunday (आइतबार), so
// nepaliGetMonthGrid uses the remapping `(canonical + 1) % 7` to map the
// canonical Monday=0 order onto Sunday=0 ... Saturday=6 columns.
//
// "Leap year" caveat: unlike the Gregorian calendar, Bikram Sambat month
// lengths are not generated by a fixed rule; each year's total length is a
// property of the underlying data table. [nepaliIsLeapYear] therefore
// treats "leap" loosely, as "longer than the most common (365-day) year"
// rather than a strict astronomical definition.

import 'package:persian_calendar/persian_calendar.dart';

