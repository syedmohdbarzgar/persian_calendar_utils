const List<String> _nepaliMonthNamesNe = [
  'बैशाख', 'जेष्ठ', 'आषाढ', 'श्रावण', 'भाद्र', 'आश्विन', //
  'कार्तिक', 'मंसिर', 'पौष', 'माघ', 'फाल्गुन', 'चैत्र',
];
const List<String> _nepaliMonthNamesEn = [
  'Baisakh', 'Jestha', 'Ashadh', 'Shrawan', 'Bhadra', 'Ashwin', //
  'Kartik', 'Mangsir', 'Poush', 'Magh', 'Falgun', 'Chaitra',
];
// Canonical order (Monday=0 ... Sunday=6)
const List<String> _weekdayNamesNe = [
  'सोमबार', 'मंगलबार', 'बुधबार', 'बिहीबार', 'शुक्रबार', 'शनिबार', //
  'आइतबार',
];
const List<String> _weekdayNamesEn = [
  'Monday', 'Tuesday', 'Wednesday', 'Thursday', 'Friday', 'Saturday', //
  'Sunday',
];

const Map<String, String> _devanagariDigitsMap = {
  '0': '०', '1': '१', '2': '२', '3': '३', '4': '४', //
  '5': '५', '6': '६', '7': '७', '8': '८', '9': '९',
};

String _toDevanagariDigits(String input) =>
    input.split('').map((c) => _devanagariDigitsMap[c] ?? c).join();

NepaliDate _todayNepali() {
  final now = DateTime.now();
  return NepaliDate.fromDate(CivilDate(now.year, now.month, now.day));
}

/// (89) Numeric display of a Nepali date using Devanagari digits, as
/// YYYY/MM/DD.
///
/// Example: `nepaliToString(NepaliDate(2081, 12, 1))` → `'२०८१/१२/०१'`
String nepaliToString(NepaliDate date) {
  final y = date.year.toString().padLeft(4, '0');
  final m = date.month.toString().padLeft(2, '0');
  final d = date.dayOfMonth.toString().padLeft(2, '0');
  return _toDevanagariDigits('$y/$m/$d');
}

/// (90) Full display of a Nepali date including weekday name and month
/// name, using Devanagari digits.
///
/// Example: `nepaliToFullString(NepaliDate(2081, 12, 1))` →
/// `'सोमबार १ फाल्गुन २०८१'` (the weekday name depends on the actual
/// computed date)
String nepaliToFullString(NepaliDate date) {
  final weekday = nepaliWeekdayName(date);
  final month = nepaliMonthName(date.month);
  final day = _toDevanagariDigits(date.dayOfMonth.toString());
  final year = _toDevanagariDigits(date.year.toString());
  return '$weekday $day $month $year';
}

/// (91) Formats a Nepali date using a custom pattern (Latin digits).
///
/// Tokens: `YYYY`, `YY`, `MMMM` (English month name), `MM`, `M`, `DDDD`
/// (English weekday name), `DD`, `D`.
///
/// Example: `nepaliFormat(NepaliDate(2081, 12, 1), 'DD MMMM YYYY')` →
/// `'01 Falgun 2081'`
String nepaliFormat(NepaliDate date, String pattern) {
  final tokens = <String, String Function()>{
    'YYYY': () => date.year.toString().padLeft(4, '0'),
    'YY': () => (date.year % 100).toString().padLeft(2, '0'),
    'MMMM': () => nepaliMonthNameEn(date.month),
    'MM': () => date.month.toString().padLeft(2, '0'),
    'M': () => date.month.toString(),
    'DDDD': () => nepaliWeekdayNameEn(date),
    'DD': () => date.dayOfMonth.toString().padLeft(2, '0'),
    'D': () => date.dayOfMonth.toString(),
  };
  final orderedKeys = tokens.keys.toList()
    ..sort((a, b) => b.length.compareTo(a.length));
  final regex = RegExp(orderedKeys.map(RegExp.escape).join('|'));
  return pattern.replaceAllMapped(regex, (m) => tokens[m[0]]!());
}

